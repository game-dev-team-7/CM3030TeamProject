/// <summary>
///     Defines the different weather types in the game.
///     Used to coordinate weather effects across various systems.
/// </summary>
public enum WeatherType
{
    /// <summary>
    ///     Hot weather condition with increased temperature effects.
    /// </summary>
    Heatwave,

    /// <summary>
    ///     Standard weather condition with no extreme temperature effects.
    /// </summary>
    Normal,

    /// <summary>
    ///     Cold weather condition with decreased temperature effects.
    /// </summary>
    Snowstorm
}